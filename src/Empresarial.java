
public class Empresarial extends Contratos {

	private int qtdFuncionarios;
	private int qtdVisitasDiarias;
	private String ramo;

	public int getQtdFuncionarios() {
		return qtdFuncionarios;
	}

	public void setQtdFuncionarios(int qtdFuncionarios) {
		this.qtdFuncionarios = qtdFuncionarios;
	}

	public int getQtdVisitasDiarias() {
		return qtdVisitasDiarias;
	}

	public void setQtdVisitasDiarias(int qtdVisitasDiarias) {
		this.qtdVisitasDiarias = qtdVisitasDiarias;
	}

	public String getRamo() {
		return ramo;
	}

	public void setRamo(String ramo) {
		this.ramo = ramo;
	}

}
